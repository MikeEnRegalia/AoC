function countTrees(input, right, down) {
    let count = 0
    let x = 0
    let y = 0

    while (true) {
        x = (x + right) % input[0].length
        y += down
        if (y > input.length - 1) {
            break;
        }
        count += input[y][x]
    }
    return count;
}

